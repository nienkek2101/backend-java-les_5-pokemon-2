public class ElectricPokemon extends Pokemon {
    String raichu = "Raichu";
    String type = "electric";

    public ElectricPokemon() {
        super();
    }

    // Getters

    public String getRaichu() {
        return raichu;
    }

    public String getType() {
        return type;
    }

    // Setters


    public void setRaichu(String raichu) {
        this.raichu = raichu;
    }
}
